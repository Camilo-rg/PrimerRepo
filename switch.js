//Switch
let nota = 5;

switch (nota){
    case 5:
        console.log("Pasas con 5");
        break;
    case 4:
        console.log("Pasas con 4");
        break;
    case 3:
        console.log("Pasas con 3");
        break;
    case 2:
        console.log("Pasas con 2");
        break;
    case 1:
        console.log("Pasas con 1");
        break;
    default:
        console.log("Agrega un valor valido");
        break;
}